            <button class="collapsible">Nutritious Recipe (Banana Heart Sisig) During ECQ (April 25, 2020)</button>
            <div class="content">
              <br>
              <p style="text-align: justify;">
              <b>Good day!</b>
              <br>
              <br>
              Simple, nutritious and tipid ulam for you and your family.
              <br>
              <br>
              <b>HOW TO MAKE BANANA HEART SISIG </b>
              <br>
              <br>
              <b>INGREDIENTS:</b>
              <br>
              <br>
              1 medium size Banana blossom (puso ng saging) cleaned and remove the outer hard part
              <br>
              1 large onion (minced)
              <br>
              4 cloves garlic (minced)
              <br>
              3 tablespoons calamansi or lemon juice
              <br>
              ¼ cup soy sauce
              <br>
              1 egg
              <br>
              Salt and pepper to taste
              <br>
              1 tablespoon mayonnaise (optional)
              <br>
              2 pcs red chili pepper (garnish)
              <br>
              <br>
              <b>PROCEDURE:</b>
              <br>
              <br>
              1.       Prepare the banana blossom and chopped it. In a pot, boil it until soft. Drain from the water and let it cool. Chopped it like any sisig (very small or almost ground). Make sure to squeeze out excess water until dry.
              <br>
              <br>
              2.       In a pan, sautéed the garlic and onion then toss in the chopped banana blossom and mix well.
              <br>
              <br>
              3.       Season with soy sauce and lemon juice. Let it simmer for seven minutes. Adjust heat to medium fire and occasionally stirring to prevent toasting.
              <br>
              <br>
              4.       Sprinkle a pinch of salt and pepper. Toss it well to incorporate your seasoning. Make a small hole in the center of the sautéed banana blossoms, then crack an egg. Turn of the heat after few seconds, allow the egg to cook on remaining heat.
              <br>
              <br>
              If you want it a little creamy, add mayonnaise once heat and turn off ( to avoid turning the mayonnaise into oil if heat is turned on) and mix well.
              <br>
              <br>
              You may add leftover chicken or pork and minced it to add variety to your sisig.
              <br>
              <br>
              <b>HEALTH BENEFITS OF BANANA HEART:</b>
              <br>
              <br>
              Ø  Rich in potassium
              <br>
              Ø  Rich in phosphorous
              <br>
              Ø  Rich in iron
              <br>
              Ø  Rich in fiber
              <br>
              Ø  Rich in antioxidants
              </p>
