            <div class="content">
              <br>
              <p style="text-align: justify;">
                <b>Good day!</b>
                <br>
                <br>
                Simple, nutritious and tipid ulam or merienda for you and your family.
                <br>
                <br>
                <b>HOW TO MAKE MONGGO BURGER</b>
                <br>
                <br>
                <b>NGREDIENTS:</b>
                <br>
                <br>
                1 cup dried monggo
                <br>
                1 pc onion (minced)
                <br>
                3 cloves garlic (minced)
                <br>
                ½ cup ketchup
                <br>
                1 cup flour
                <br>
                3 eggs (beaten)
                <br>
                1 tsp sugar
                <br>
                Cooking oil
                <br>
                Salt and pepper to taste
                <br>
                <br>
                <b>PROCEDURE:</b>
                <br>
                <br>
                1.       Simmer monggo beans in hot water for an hour then remove the excess water.
                <br>
                <br>
                2.       Add the rest of the ingredients mix and form patties according to your desired size.
                <br>
                <br>
                3.       Fry mixture until golden brown.
                <br>
                <br>
                4.       Remove patties and drain on a plate lined with paper towels.
                <br>
                <br>
                <b>HEALTH BENEFITS OF MONGGO BEANS:</b>
                <br>
                <br>
                Ø  Full of protein and fiber
                <br>
                Ø  Lowers blood pressure
                <br>
                Ø  Great source of folate
                <br>
                Ø  Boosts immunity to protect against infections
                <br>
                Ø  Helps reduce weight and fight obesity
                <br>
                Ø  Controls cholesterol and heart disease risk
              </p>
