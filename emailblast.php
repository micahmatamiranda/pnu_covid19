            <button class="collapsible">PNU COVID Watch # 2 (March 6, 2020)</button>
            <div class="content" style="text-align: center;">
              <br>
              <p style="text-align: justify;">
              With the recent developments on the COVID-19 outbreak and the increasing number of incidents not only in the Philippines but around the globe, the Philippine Normal University continues to exercise vigilance and proactive measures to prevent the spread of the virus.
              <br>
              <br>
              Faculty members, administrative staff, and students are still encouraged to limit their activities, especially outside the University, to avoid exposure. Official travel to countries with or without confirmed cases will likewise be very limited.<br>
              <br>
              For those who have recently travelled to countries with confirmed cases and have experienced symptoms of severe respiratory illness or other signs of COVID-19, please seek medical attention immediately.
              <br>
              <br>
              Safety mechanisms and protective measures
              <br>
              <br>
              1. Gate 1 and Gate 2 of the Main Entrance and HRD Gate will be used for entry and exit. Access to the other gates will be very limited. Medical personnel are on standby on both gates to do temperature check and, in case of emergency, provide immediate medical attention to the individual showing symptoms. Rubbing alcohol and sanitizers are also available in strategic places around the campus.
              <br>
              <br>
              2. The University Health Services Unit (UHSU) has a limited supply of protective gear; thus faculty members, administrative staff, and students are encouraged to provide for their own.
              <br>
              <br>
              3. Prevention is better than cure. Always practice good personal hygiene and a healthy lifestyle. Wash hands frequently with soap and water and disinfect with alcohol-based solutions whenever possible. Avoid touching eyes, nose, and mouth to prevent contamination. Make sure to follow good respiratory hygiene. This means covering the mouth and nose with a bent elbow or a tissue when coughing or sneezing and then disposing of the used tissue paper immediately.
              <br>
              <br>
              4. Boost the immune system by eating healthy food and taking vitamins, if any.
              <br>
              <br>
              5. Maintain social distancing. Make sure to leave at least 1 meter distance from anyone who is coughing and sneezing.
              <br>
              <br>
              6. Stay calm but vigilant at all times. Do not panic.
              <br>
              <br>
              7. Anyone experiencing any symptoms of the COVID-19 is advised to seek medical attention immediately and stay home to rest.
              <br>
              <br>
              The University is currently monitoring the situation with the help of other government agencies. Further announcements regarding the COVID-19 will be released once available. 
              <br>
              <br>
              For concerns, inquiries, and assistance, please contact the UHSU through (+63 2) 5317.1768 loc. 592 or via email at uhsu@pnu.edu.ph.
