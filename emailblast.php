            <button class="collapsible">30 Minutes Nutritious Recipe During COVID-19 Quarantine Period (April 12, 2020)</button>
            <div class="content">
              <br>
              <p>
                <b>Good day!</b>
                <br>
                <br>
                Simple, nutritious and tipid ulam or merienda for you and your family.
                <br>
                <br>
                <b>HOW TO MAKE CHICKEN CAMOTE NUGGETS WITH SQUASH & MALUNGGAY LEAVES</b>
                <br>
                <br>
                <b>Ingredients:</b>
                <br>
                <br>
                ¼ kilo camote
                <br>
                ¼ kilo kalabasa (squash)
                <br>
                1/8 cup malunggay leaves
                <br>
                ¼ kilo chicken breast giniling
                <br>
                1 pc medium size onion (chopped finely)
                <br>
                1 pc egg (beaten)
                <br>
                ¼ cup flour
                <br>
                ½ cup breadcrumbs
                <br>
                1 cup cooking oil
                <br>
                Salt & pepper to taste
                <br>
                <br>
                <b>Methods:</b>
                <br>
                <br>
                1.     Peel camote and kalabasa and bring a large of water to boil, add the camote and kalabasa and cook for around 15-20 minutes. Make sure they are slightly softened and not easily mashed.
                <br>
                <br>
                2.     Drain and let it cool then grate the camote and kalabasa using a cheese grater. In a mixing bowl combine the grated camote & kalabasa, malunggay leaves, chopped onion, flour, ground black pepper, and salt until thoroughly mixed.
                <br>
                <br>
                3.     Form two teaspoon of mixture into a ball and shape into nuggets.
                <br>
                <br>
                4.     Do the same thing to the rest of the mixture.
                <br>
                <br>
                5.     Dip in beaten egg then roll in breadcrumbs.
                <br>
                <br>
                6.      Deep fry until golden brown.
                <br>
                <br>
                Spiced vinegar, ketchup or mayonnaise for your sauce.
                <br>
                <br>
                <b>Happy cooking & stay healthy!</b>
              </p>
