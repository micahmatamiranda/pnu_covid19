            <button class="collapsible">Facts About COVID-19 Contact Tracing (March 13, 2020)</button>
            <div class="content">
              <br>
              <p>Dear PNU Community,</p>
              <p>The rising number of confirmed cases of COVID19 in Metro Manila may require contact tracing in specific cases. Thus, it is important to be informed. Below are some facts about contact tracing.</p>
              <p><strong>FACTS ABOUT CONTACT TRACING:</strong></p>
              <ol>
                <li>Contact tracing is the identification, listing, and follow-up of persons who may have come into contact with COVID19 infected person.</li>
                <li>Close contact is anyone who may have come into contact with an infected person</li>
                <li>Person Under Investigation (PUI) refers to any person who exhibits fever and/or cough and colds and has at least one of the following, and shall be referred to hospital quarantine:
                  <ul>
                    <li>history of travel to areas with COVID19 14 days prior to onset of symptoms</li>
                    <li>has a history of exposure 14 days prior to onset of symptoms</li>
                  </ul>
                </li>
                <li>Person Under Monitoring (PUM) refers to any person who does not exhibit any sign nor symptom, has history of exposure, and shall be referred to home quarantine</li>
              </ol>
              <p>Please read attached DOH Interim Guidelines on 2019-nCoV ARD Contact Tracing.</p>
