            <button class="collapsible">COVID-19 Signs and Symptoms and Community Transmission (March 19, 2020)</button>
            <div class="content">
              <br>
              <p>Dear PNU Community,</p>
              <p>DOH data show a growing number of community transmission of COVID-19. Community transmission means that an illness spreads in such a way that the source of its infection is not traceable. An example is the case of Christopher De Leon, who has not traveled outside the Philippines nor has been exposed to or in close contact with a COVID-19 positive patient but has tested positive.</p>
              <p>WHO has identified the following most common signs and symptoms of COVID-19:</p>
              <ul>
                <li>Fever</li>
                <li>Tiredness</li>
                <li>Dry Cough</li>
              </ul>
              <p>Some patients may experience runny nose, nasal congestion, sneezing, body aches and pains, sore throat or diarrhea. Most patients manifest mild symptoms that develop gradually and recover without needing special treatment. Very few develop difficulty of breathing and most of them have other illnesses.</p>
              <p>If you experience any of these signs and symptoms please go to the nearest hospital or call <strong>DOH COVID-19 Emergency hotline 02-894-COVID (02-894-26843)</strong>.</p>
